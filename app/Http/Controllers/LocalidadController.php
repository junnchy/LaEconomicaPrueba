     * @return \Illuminate\Http\Response
     */
    public function index(Request $request)
    {
        if($request->ajax()){
            $localidades = Localidad::with('provincia')
            ->orderBy('provincia_id')
            ->orderBy('nombre')
            ->get();
            return response()->json($localidades);
